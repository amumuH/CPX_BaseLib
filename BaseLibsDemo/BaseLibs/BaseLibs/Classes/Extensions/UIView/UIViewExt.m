{
    CGPoint center = self.center;
    center.y = centerY;
    self.center = center;
}

- (CGFloat)centerY
{
    return self.center.y;
}

- (UIView *)gridViewsCustom:(NSArray *)views inColumn:(NSInteger)column size:(CGSize)cellSize margin:(UIEdgeInsets)margin inRect:(CGRect)rect
{
    CGSize menuSize = cellSize;
    NSLog(@"rect = %f,%f,%f,%f",rect.origin.x,rect.origin.y,rect.size.height,rect.size.width);
    const NSInteger kColumn = column;
    const NSInteger kCount = views.count;
    const NSInteger kRow = kCount % kColumn == 0 ? kCount / kColumn : kCount / kColumn + 1;
    
//    const CGFloat kTop = margin.top;
    const CGFloat kleft = margin.left;
    const CGFloat kbottom = margin.bottom;
    const CGFloat kright = margin.right;
    
    CGRect menuCellRect = rect;
    menuCellRect.size = menuSize;
    
    CGFloat kmiddleLess = (rect.size.width - kleft - kright - (menuSize.width * column))/(column - 1);
    if (kmiddleLess <= 0) {
        kmiddleLess = 1;
    }
    
    for (NSInteger i = 0; i < kRow; i++)
    {
        for (NSInteger j = 0; j < kColumn; j++)
        {
            NSInteger index = i * kColumn + j;
            if (index >= kCount)
            {
                return self;
            }
            
            UIView *view = [views objectAtIndex:index];
            
            if (j == 0) {
                
                menuCellRect.origin.x += kleft;//x距离  第一个
            }
            else
            {
                menuCellRect.origin.x += kmiddleLess;
            }
            
            if (j == kColumn -1) {
                menuCellRect.origin.x = rect.size.width - kright - menuSize.width;
            }
            
            view.frame = menuCellRect;
            menuCellRect.origin.x += menuCellRect.size.width;
            
        }
        
        menuCellRect.origin.x = rect.origin.x;
        menuCellRect.origin.y += menuCellRect.size.height + kbottom;
    }
    
    return self;
}


@end
